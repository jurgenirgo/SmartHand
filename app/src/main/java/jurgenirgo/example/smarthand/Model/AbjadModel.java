package jurgenirgo.example.smarthand.Model;

public class AbjadModel {

    int imageAbjad, imageJari;

    public AbjadModel(int imageAbjad, int imageJari) {
        this.imageAbjad = imageAbjad;
        this.imageJari = imageJari;
    }

    public int getImageAbjad() {
        return imageAbjad;
    }

    public int getImageJari() {
        return imageJari;
    }
}
